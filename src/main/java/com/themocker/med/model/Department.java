package com.themocker.med.model;

public class Department {

  private long depNo;
  private long depHosNo;
  private String depName;
  private String depCall;


  public long getDepNo() {
    return depNo;
  }

  public void setDepNo(long depNo) {
    this.depNo = depNo;
  }


  public long getDepHosNo() {
    return depHosNo;
  }

  public void setDepHosNo(long depHosNo) {
    this.depHosNo = depHosNo;
  }


  public String getDepName() {
    return depName;
  }

  public void setDepName(String depName) {
    this.depName = depName;
  }


  public String getDepCall() {
    return depCall;
  }

  public void setDepCall(String depCall) {
    this.depCall = depCall;
  }

}
