package com.themocker.med.model;


public class Muser {

  private long muserNo;
  private String muserAccount;
  private String muserPassword;
  private String muserName;


  public long getMuserNo() {
    return muserNo;
  }

  public void setMuserNo(long muserNo) {
    this.muserNo = muserNo;
  }


  public String getMuserAccount() {
    return muserAccount;
  }

  public void setMuserAccount(String muserAccount) {
    this.muserAccount = muserAccount;
  }


  public String getMuserPassword() {
    return muserPassword;
  }

  public void setMuserPassword(String muserPassword) {
    this.muserPassword = muserPassword;
  }


  public String getMuserName() {
    return muserName;
  }

  public void setMuserName(String muserName) {
    this.muserName = muserName;
  }

}
